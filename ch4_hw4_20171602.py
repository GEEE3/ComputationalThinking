# 아래의 문장을 역순으로 저장하여 출력하는 script 작성 할 것

# 1. original = "This class is Computational Thinking!"
original = "This class is Computational Thinking!"

# 2. 문자열의 slicing 사용
reverse = original[::-1]

# 3. 문자열의 메소드 format()을 사용하여 출력할 것
print("reversed sentence : {}" .format(reverse))